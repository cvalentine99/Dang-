#!/usr/bin/env node
/**
 * generate-wiring-ledger.mjs
 * 
 * Scans the wazuh router for procedure names and the client for UI callsites.
 * Produces docs/wiring-ledger.md with file + line number for every procedure.
 */
import { readFileSync, writeFileSync, readdirSync, statSync } from "fs";
import { join, dirname, relative } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const CLIENT_SRC = join(ROOT, "client", "src");

// ── Extract procedure names from wazuh router ──────────────────────────────
const routerPath = join(ROOT, "server", "wazuh", "wazuhRouter.ts");
const routerSrc = readFileSync(routerPath, "utf-8");
const routerLines = routerSrc.split("\n");

// Match lines like "  procedureName: wazuhProcedure" or "  procedureName: protectedProcedure"
const SKIP = new Set(["limit", "offset", "path", "responsePromise", "brokerErrors", "data", "error"]);
const procedures = [];
for (let i = 0; i < routerLines.length; i++) {
  const m = routerLines[i].match(/^\s{2,4}(\w+):\s*(wazuh|public|protected|admin)Procedure/);
  if (m && !SKIP.has(m[1])) {
    procedures.push({ name: m[1], routerLine: i + 1 });
  }
}

// ── Scan all client .tsx/.ts files for trpc.wazuh.X.useQuery callsites ─────
function walkDir(dir) {
  const results = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      results.push(...walkDir(full));
    } else if (entry.name.endsWith(".tsx") || entry.name.endsWith(".ts")) {
      results.push(full);
    }
  }
  return results;
}

const clientFiles = walkDir(CLIENT_SRC);
const callsiteMap = new Map(); // procedureName -> [{file, line}]

for (const filePath of clientFiles) {
  const src = readFileSync(filePath, "utf-8");
  const lines = src.split("\n");
  for (let i = 0; i < lines.length; i++) {
    const matches = lines[i].matchAll(/trpc\.wazuh\.(\w+)\.(useQuery|useMutation)/g);
    for (const m of matches) {
      const procName = m[1];
      if (!callsiteMap.has(procName)) callsiteMap.set(procName, []);
      callsiteMap.get(procName).push({
        file: relative(ROOT, filePath),
        line: i + 1,
      });
    }
  }
}

// ── Build ledger ───────────────────────────────────────────────────────────
let wiredCount = 0;
let unwiredCount = 0;

const lines = [];
lines.push("# Wiring Ledger");
lines.push("");
lines.push("> **Machine-generated by `scripts/generate-wiring-ledger.mjs`**");
lines.push("> Scans `server/wazuh/wazuhRouter.ts` for procedure definitions and");
lines.push("> `client/src/**/*.{ts,tsx}` for `trpc.wazuh.*.useQuery` callsites.");
lines.push("> To regenerate: `node scripts/generate-wiring-ledger.mjs`");
lines.push("");
lines.push(`Generated: ${new Date().toISOString()}`);
lines.push("");

lines.push("## Summary");
lines.push("");

// Count first
for (const proc of procedures) {
  if (callsiteMap.has(proc.name)) wiredCount++;
  else unwiredCount++;
}

lines.push(`| Metric | Count |`);
lines.push(`|--------|-------|`);
lines.push(`| Total procedures | ${procedures.length} |`);
lines.push(`| Wired (UI callsite exists) | ${wiredCount} |`);
lines.push(`| Unwired (no UI callsite) | ${unwiredCount} |`);
lines.push(`| Parity | ${wiredCount}/${procedures.length} |`);
lines.push("");

lines.push("## Full Ledger");
lines.push("");
lines.push("| # | Procedure | Status | Router Line | UI Callsite(s) |");
lines.push("|---|-----------|--------|-------------|----------------|");

for (let i = 0; i < procedures.length; i++) {
  const proc = procedures[i];
  const sites = callsiteMap.get(proc.name);
  if (sites && sites.length > 0) {
    const siteStr = sites.map(s => `\`${s.file}:${s.line}\``).join(", ");
    lines.push(`| ${i + 1} | \`${proc.name}\` | **Wired** | L${proc.routerLine} | ${siteStr} |`);
  } else {
    lines.push(`| ${i + 1} | \`${proc.name}\` | Unwired | L${proc.routerLine} | — |`);
  }
}

lines.push("");

if (unwiredCount > 0) {
  lines.push("## Unwired Procedures");
  lines.push("");
  for (const proc of procedures) {
    if (!callsiteMap.has(proc.name)) {
      lines.push(`- \`${proc.name}\` (router L${proc.routerLine})`);
    }
  }
  lines.push("");
}

// Check for UI calls to procedures not in the router
const routerProcNames = new Set(procedures.map(p => p.name));
const orphanCalls = [];
for (const [procName, sites] of callsiteMap) {
  if (!routerProcNames.has(procName)) {
    orphanCalls.push({ name: procName, sites });
  }
}
if (orphanCalls.length > 0) {
  lines.push("## Orphan UI Calls (no matching router procedure)");
  lines.push("");
  for (const o of orphanCalls) {
    const siteStr = o.sites.map(s => `\`${s.file}:${s.line}\``).join(", ");
    lines.push(`- \`${o.name}\`: ${siteStr}`);
  }
  lines.push("");
}

lines.push("---");
lines.push("");
lines.push("*End of machine-generated ledger. Source of truth: `server/wazuh/wazuhRouter.ts` + `client/src/**/*`*");
lines.push("");

writeFileSync(join(ROOT, "docs", "wiring-ledger.md"), lines.join("\n"));

// Also write JSON version
const jsonLedger = {
  generated: new Date().toISOString(),
  totalProcedures: procedures.length,
  wired: wiredCount,
  unwired: unwiredCount,
  parity: `${wiredCount}/${procedures.length}`,
  procedures: procedures.map(p => ({
    name: p.name,
    routerLine: p.routerLine,
    wired: callsiteMap.has(p.name),
    callsites: (callsiteMap.get(p.name) || []).map(s => `${s.file}:${s.line}`),
  })),
  orphanCalls: orphanCalls.map(o => ({
    name: o.name,
    callsites: o.sites.map(s => `${s.file}:${s.line}`),
  })),
};
writeFileSync(join(ROOT, "docs", "wiring-ledger.json"), JSON.stringify(jsonLedger, null, 2));

console.log(`Wiring ledger written to docs/wiring-ledger.md + docs/wiring-ledger.json`);
console.log(`  Total: ${procedures.length} | Wired: ${wiredCount} | Unwired: ${unwiredCount}`);
if (orphanCalls.length > 0) {
  console.log(`  WARNING: ${orphanCalls.length} orphan UI calls found (no matching router procedure)`);
}
